            ViewBag.ReturnUrl = returnUrl;
            return View("_PartialLogin");
        }

        //
        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {
            if (ViewBag.ReturnUrl == null)
                ViewBag.ReturnUrl = returnUrl;
            else
                returnUrl = ViewBag.ReturnUrl; 
            

            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            var result = await SignInManager.PasswordSignInAsync(model.Email, model.Password, model.RememberMe, shouldLockout: false);
            switch (result)
            {
                case SignInStatus.Success:
                    return RedirectToLocal(returnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = model.RememberMe });
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Tentativa de login inválida.");
                    return View(model);
            }
        }

        //
        // GET: /Account/VerifyCode
        [AllowAnonymous]
        public async Task<ActionResult> VerifyCode(string provider, string returnUrl, bool rememberMe)
        {
            // Require that the user has already logged in via username/password or external login
            if (!await SignInManager.HasBeenVerifiedAsync())
            {
                return View("Error");
            }
            return View(new VerifyCodeViewModel { Provider = provider, ReturnUrl = returnUrl, RememberMe = rememberMe });
        }

        //
        // POST: /Account/VerifyCode
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> VerifyCode(VerifyCodeViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // The following code protects for brute force attacks against the two factor codes. 
            // If a user enters incorrect codes for a specified amount of time then the user account 
            // will be locked out for a specified amount of time. 
            // You can configure the account lockout settings in IdentityConfig
            var result = await SignInManager.TwoFactorSignInAsync(model.Provider, model.Code, isPersistent: model.RememberMe, rememberBrowser: model.RememberBrowser);
            switch (result)
            {
                case SignInStatus.Success:
                    return RedirectToLocal(model.ReturnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Código inválido.");
                    return View(model);
            }
        }

        //
        // GET: /Account/Register
        [AllowAnonymous]
        public ActionResult Register(string returnUrl)
        {
            ViewBag.ReturnUrl = returnUrl;
            return View();
        }

        //
        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model, string returnUrl)
        {
            ViewBag.ReturnUrl = returnUrl;
            if (!ModelState.IsValid) return View(model);
            var user = new ApplicationUser { UserName = model.Email, Email = model.Email };
            var result = await UserManager.CreateAsync(user, model.Password);
            if (result.Succeeded)
            {
                UserManager.AddToRole(user.Id, "Cliente");

                await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);

                await SendEmail(user.Id, returnUrl);

                return RedirectToAction("VerifyEmail");
            }
            AddErrors(result);

            // Se chegarmos até aqui, algo falhou, redisplay formulário
            return View(model);
        }

        public async Task<ActionResult> SendEmail(string userId, string returnUrl = "")
        {
            // Para obter mais informações sobre como ativar a confirmação da conta e a redefinição de senha, acesse http://go.microsoft.com/fwlink/?LinkID=320771
            // Envie um email com este link
            string code = await UserManager.GenerateEmailConfirmationTokenAsync(userId);
            var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = userId, code = code },
                protocol: Request.Url.Scheme) + (string.IsNullOrEmpty(returnUrl) ? "" : "&returnUrl=" + returnUrl);
            await
                UserManager.SendEmailAsync(userId, "Confirmação de conta",
                    "Por favor confirme sua conta clicando aqui: <a href=\"" + callbackUrl + "\">Aqui</a>");
            return RedirectToAction("VerifyEmail");
        }

        public ActionResult VerifyEmail()
        {
            return View();
        }

        //
        // GET: /Account/ConfirmEmail
        [AllowAnonymous]
        public async Task<ActionResult> ConfirmEmail(string userId, string code, string returnUrl)
        {
            if (userId == null || code == null)
            {
                return View("Error", new HandleErrorInfo(new System.Exception("Código de validação inválido"), "Conta usuário", "Comfirmação de Email"));
            }
            try
            {
                var result = await UserManager.ConfirmEmailAsync(userId, code);
                if (result.Succeeded && !string.IsNullOrEmpty(returnUrl))
                    return Redirect(returnUrl);
                return View(result.Succeeded ? "ConfirmEmail" : "Error");
            }
            catch (System.Exception)
            {
                return View("Error", new HandleErrorInfo(new System.Exception("Código de validação inválido"), "Conta usuário", "Confirmação de Email"));
            }
            
        }

