        [AllowAnonymous]
        public ActionResult ForgotPassword()
        {
            return View();
        }

        //
        // POST: /Account/ForgotPassword
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> ForgotPassword(ForgotPasswordViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = await UserManager.FindByNameAsync(model.Email);
                if (user == null || !(await UserManager.IsEmailConfirmedAsync(user.Id)))
                {
                    // Não revele que o usuário não existe ou não está confirmado
                    return View("ForgotPasswordConfirmation");
                }

                // Para obter mais informações sobre como ativar a confirmação da conta e a redefinição de senha, acesse http://go.microsoft.com/fwlink/?LinkID=320771
                // Envie um email com este link
                string code = await UserManager.GeneratePasswordResetTokenAsync(user.Id);
                var callbackUrl = Url.Action("ResetPassword", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                await UserManager.SendEmailAsync(user.Id, "Reset Password", "Por favor, para resetar sua senha clique aqui <a href=\"" + callbackUrl + "\">TROCAR SENHA</a>");
                return RedirectToAction("ForgotPasswordConfirmation", "Account");
            }

            // Se chegarmos até aqui, algo falhou, redisplay formulário
            return View(model);
        }

